#string slicing
print("welcome to string slicing")
name="harshit"
#positive slicing
print(len(name))
print(name[:6])
print(name[0:6]) #0 is skipable, including 0 but not including 4
print(name[1:6])
print(name[:])
#nagative slicing
print(name[0:-6])
print(name[-1:-6]) #that is execute a null 
print(name[-6:-1])
print(name[:-1])
print(name[-1:])
print(name[::-1])  # reversing slicing
print(name[-6:-4])
